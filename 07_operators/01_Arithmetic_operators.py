a = 34
b = 2

# Arithmetic Operators
print(" a + b = ", a + b)
print(" a - b = ", a - b)
print(" a * b = ", a * b)
print(" a / b = ", a / b)
print(" a % b = ", a % b)
print(" a // b = ", a // b)  # floor division operator means integer division for example 34//2 = 17
print(" a ** b = ", a ** b)  # a^b = a to the power of b


